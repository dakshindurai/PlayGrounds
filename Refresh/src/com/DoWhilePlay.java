package com;

public class DoWhilePlay {
    public static void main(String[] args){
        int i = 3;
        do {
            System.out.println("DoWhile Value of I "+ i);
            i++;
        }while (i < 12);
        while (i <= 12){
            System.out.println("While Value of I "+ i);
            i++;
        }
    }
}
