package com;

public class SwitchCasePlay {
    public static void main(String[] args){
        int i = 5;
        switch (i){
            case 1:
                System.out.println("The value of i = 1");
                break;
            case 2:
                System.out.println("The value of i = 2");
                break;
            case 3:
                System.out.println("The value of i = 3");
                break;
            case 4:
                System.out.println("The value of i = 4");
                break;
            case 5:
                System.out.println("The value of i = 5");
                break;
            case 6:
                System.out.println("The value of i = 6");
                break;
            case 7:
                System.out.println("The value of i = 7");
                break;

        }
    }
}
